/**
 * One-time script to create medication-images Supabase Storage bucket
 * Run this once to set up the bucket for medication image uploads
 */

const { createClient } = require('@supabase/supabase-js');

// Get Supabase credentials from environment
const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL;
const supabaseServiceKey = process.env.SUPABASE_SERVICE_ROLE_KEY || process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY;

if (!supabaseUrl || !supabaseServiceKey) {
  console.error('‚ùå Error: Supabase credentials not found in environment variables');
  console.error('Make sure NEXT_PUBLIC_SUPABASE_URL and SUPABASE_SERVICE_ROLE_KEY are set');
  process.exit(1);
}

const supabase = createClient(supabaseUrl, supabaseServiceKey);

async function createMedicationBucket() {
  try {
    console.log('üöÄ Creating medication-images storage bucket...');

    // Check if bucket already exists
    const { data: buckets } = await supabase.storage.listBuckets();
    const bucketExists = buckets?.some(b => b.id === 'medication-images');

    if (bucketExists) {
      console.log('‚úÖ Bucket "medication-images" already exists!');
      return;
    }

    // Create the bucket
    const { error } = await supabase.storage.createBucket('medication-images', {
      public: true,
      fileSizeLimit: 3145728, // 3MB
      allowedMimeTypes: ['image/jpeg', 'image/jpg', 'image/png', 'image/webp']
    });

    if (error) {
      console.error('‚ùå Error creating bucket:', error);
      process.exit(1);
    }

    console.log('‚úÖ Successfully created medication-images bucket!');
    console.log('üì¶ Bucket settings:');
    console.log('   - Public: Yes');
    console.log('   - Max size: 3MB');
    console.log('   - Allowed types: JPG, PNG, WebP');

  } catch (error) {
    console.error('‚ùå Unexpected error:', error);
    process.exit(1);
  }
}

createMedicationBucket();
